    private void bt_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editarActionPerformed
        // TODO add your handling code here:
        if(tf_proveedorID.getText().equals("") || tf_nombre.getText().equals("") || tf_telefono.getText().equals("") || tf_cedula.getText().equals("") || tf_sexo.getText().equals("") || tf_email.getText().equals("") || ta_direccion.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Todos los campos son requeridos!", 
                    "ERROR", JOptionPane.WARNING_MESSAGE);
        }
        else{
             if(tf_cedula.getText().length() > 30){
                mostrarLength("Cedula",30 , tf_cedula.getText().length());
            }else if(tf_nombre.getText().length() > 50){
                mostrarLength("Nombre",50 , tf_nombre.getText().length());
            }else if(tf_telefono.getText().length() > 20){
                mostrarLength("Telefono", 20, tf_telefono.getText().length());
            }else if(tf_email.getText().length() > 30){
                mostrarLength("Email", 30, tf_email.getText().length());
            }else if(ta_direccion.getText().length() > 200){
                mostrarLength("Direccion", 200, ta_direccion.getText().length());
            }else{
                proveedor.setProveedor_id(Integer.parseInt(tf_proveedorID.getText()));
                proveedor.setNombre(tf_nombre.getText());
                proveedor.setTelefono(tf_telefono.getText());
                proveedor.setCedula(tf_cedula.getText());
                proveedor.setSexo(Integer.parseInt(tf_sexo.getText()));
                proveedor.setEmail(tf_email.getText());
                proveedor.setDireccion(ta_direccion.getText());
            }
                

                if(dt_proveedor.editarProveedor(proveedor)){
                    JOptionPane.showMessageDialog (this, "El registro fue actualizado con éxito!", 
                      "MENSAJE", JOptionPane.INFORMATION_MESSAGE);
                    actualizarTabla();
                    limpiarCampos();
                }
                else{
                    JOptionPane.showMessageDialog(this, 
                      "Revise los datos e intente nuevamente. Si el error persiste contacte al Administrador del Sistema.", 
                      "ERROR", JOptionPane.ERROR_MESSAGE);
         }

    }//GEN-LAST:event_bt_editarActionPerformed
}
